
public class Taco {
	
	
private String meat;
private String sauce;

Taco(String meat, String sauce) {
	this.meat = meat;
	this.sauce = sauce;
}
public String getMeat() {
	return meat;
}
public String getSauce() {
	return sauce;
}
public void setMeat(String Meat) {
	this.meat = Meat;
}
public void setSauce(String Sauce) {
	this.sauce = Sauce;
}
}
